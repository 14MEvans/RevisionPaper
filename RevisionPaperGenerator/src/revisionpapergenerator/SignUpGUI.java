    //Public method so it can be used through different files. 
    public static String getPass(JPasswordField pfPassword) {
        char[] PasswordChars = pfPassword.getPassword();

        //Initialise Password
        String Password = "";
        //Change from Char to String 
        for (char PasswordChar : PasswordChars) {
            Password += PasswordChar;
            PasswordChar = 0;
        }
        return Password;
    }

