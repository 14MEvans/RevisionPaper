    private void btnSignUpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSignUpActionPerformed
        //Declaring variables
        this.Username = tfUsername.getText();
        String errors = "";
        String userEmail = tfEmail.getText();
        //Getting data from both password fields
        password = getPass(pfPassword);
        String passwordC = getPass(pfPasswordC);

        //Connecting to the database
        DatabaseConnect myConn = null;

        try {
            myConn = new DatabaseConnect();
            //Catches any SQL errors
        } catch (SQLException ex) {
            Logger.getLogger(LoginGUI.class.getName()).log(Level.SEVERE, null, ex);
        }
        //Searching for already existing usernames
        String sQuery = "SELECT Username FROM tblUser WHERE Username = '" + Username + "'";
        ResultSet rs = null;
        try {
            //Running the query
            rs = myConn.runSelectQuery(sQuery);
        } catch (SQLException ex) {
            Logger.getLogger(SignUpGUI.class.getName()).log(Level.SEVERE, null, ex);
        }

        //Validation on usernames and SQL Injection protection
        if (Username.length() < 5 || Username.contains("'") || Username.contains("?")
                || Username.contains("=")) {
            errors += "Please enter a username of at least 5 characters with no "
                    + "harmful characters (â€˜ ? or = symbols) <br>";
            //Resetting username
            Username = "";
        } else {
            try {
                if (rs.next()) {
                    errors += "Username already exists. Please enter a different username. <br>";
                    Username = "";
                }
            } catch (SQLException ex) {
                Logger.getLogger(SignUpGUI.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

        //Validating Passwords
        if (!password.equals(passwordC) || password.length() < 8) {
            errors += "Both passwords must be the same and contain at least 7 characters. <br>";
            password = "";
        }

        //Validating email address
        if (!userEmail.contains("@") || !userEmail.contains(".")) {
            errors += "Please enter a valid email address <br>";
            userEmail = "";
        }

        //displaying errors
        lblInfo.setText("<html>" + errors + "</html>");
        if (Username.length() > 1 && password.length() > 1 && userEmail.length() > 1) {
            sendRegEmail();
            //opening the new form
            setVisible(false);
            new ConfirmSignUpGUI().setVisible(true);
        }

    }//GEN-LAST:event_btnSignUpActionPerformed

